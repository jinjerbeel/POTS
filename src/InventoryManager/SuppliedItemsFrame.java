    private void btnFilterSuppliedItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterSuppliedItemsActionPerformed

        String itemCodeInput = (String) cbxSuppliedItem.getSelectedItem();
        String supplierIDInput = (String) cbxSupplier.getSelectedItem();

        boolean viewAllCostPrice = chkViewAllCostPrice.isSelected();
        String minCostPriceInput = txtMinCostPrice.getText();
        String maxCostPriceInput = txtMaxCostPrice.getText();
        

        List<String> matchingRows = SuppliedItems.filterSuppliedItemsTable(
            supplierIDInput,
            itemCodeInput,
            viewAllCostPrice,
            minCostPriceInput,
            maxCostPriceInput);

        DefaultTableModel tableModel = (DefaultTableModel) tblSuppliedItems.getModel();

        tableModel.setRowCount(0);

        if (!matchingRows.isEmpty()) {
            for(String row : matchingRows) {
                String[] parts = row.split(",");
                String supplierCode = parts[0].trim();
                String itemCode = parts[1].trim();
                String cost = (parts.length > 2) ? parts[2].trim() : ""; 
                tableModel.addRow(new Object[]{supplierCode,itemCode,cost});

            }
            lblNoMatchMsg.setVisible(false);
        } else {
            lblNoMatchMsg.setVisible(true);
            lblNoMatchMsg.setText("No Matching Items");
        }
    }//GEN-LAST:event_btnFilterSuppliedItemsActionPerformed

