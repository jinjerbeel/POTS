    private void btnConfirmModifySuppliedItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmModifySuppliedItemsActionPerformed
        String itemCode = (String) cbxModifySuppliedItems.getSelectedItem();
        String supplierCode = (String) cbxModifySupplier.getSelectedItem();
        String costStr = txtModifySuppliedItemCost.getText().trim();
        
        supplierList.clear();
        supplierManager.read(supplierList, Supplier.class);
        suppliedItemsList.clear();
        suppliedItemsManager.read(suppliedItemsList, SuppliedItems.class);
        
        // Validation checks
        if(supplierCode.equals("NONE") || itemCode.equals("NONE")){
            JOptionPane.showMessageDialog(this, "Select both supplier & item to modify.");
            return;     
        }

        double cost;

        try{
            cost = Double.parseDouble(costStr);
            if (cost < 0) { // Check for negative cost or selling price
                JOptionPane.showMessageDialog(this, "Cost cannot be negative.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Cost must be valid numbers.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean success = SuppliedItems.updateCost(suppliedItemsList, supplierCode, itemCode,cost);
        
        if (success) {
            loadSuppliedItemsTable();
            JOptionPane.showMessageDialog(this, "Item modified successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Supplied Item does not exist.","Input Error",JOptionPane.ERROR_MESSAGE);
        }
                   
    }//GEN-LAST:event_btnConfirmModifySuppliedItemsActionPerformed

