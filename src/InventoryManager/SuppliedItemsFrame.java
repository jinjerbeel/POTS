    private void loadSuppliedItemsTable(){
        suppliedItemsList.clear();
        // Load items from the file into the ArrayList
        SuppliedItems.rebuildSuppliedItems();
        suppliedItemsManager.read(suppliedItemsList, SuppliedItems.class);
        // Add items to the JTable
        DefaultTableModel model = (DefaultTableModel) tblSuppliedItems.getModel();
        model.setRowCount(0);  // Clear existing rows

        for (SuppliedItems item : suppliedItemsList) {
            model.addRow(new Object[]{
                item.getSupplierID(), 
                item.getItemCode(), 
                item.getCost()
            });
        }

        // Optionally, handle case when no items are found
        if (suppliedItemsList.isEmpty()) {
            lblNoMatchMsg.setVisible(true); // Show no items found message
        } else {
            lblNoMatchMsg.setVisible(false); // Hide the message
        }
    }
    
