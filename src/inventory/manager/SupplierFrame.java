    private void btnFilterSuppliersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterSuppliersActionPerformed

        String supplierCodeInput = txtSupplierCode.getText();
        String supplierNameInput = (String) cbxSupplierName.getSelectedItem();
        String headquartersInput = (String) cbxHeadquaters.getSelectedItem();
        String itemIDInput = txtItemID.getText();

        List<String> matchingRows = Supplier.filterSuppliersTable(supplierCodeInput, supplierNameInput, headquartersInput, itemIDInput);

        DefaultTableModel tableModel = (DefaultTableModel) tblSupplierTable.getModel();

        tableModel.setRowCount(0);

        if (!matchingRows.isEmpty()) {
            for (String row : matchingRows) {
                String[] parts = row.split(",");
                String supplierCode = parts[0].trim();
                String supplierName = parts[1].trim();
                String headquarters = parts[2].trim();
                String itemCodesString = String.join(";", Arrays.asList(parts).subList(3, parts.length));

                tableModel.addRow(new Object[]{supplierCode, supplierName, headquarters, itemCodesString});
            }
            lblNoMatchMsg.setVisible(false);
        } else {
            lblNoMatchMsg.setVisible(true);
            lblNoMatchMsg.setText("No Matching Items");
        }
    }//GEN-LAST:event_btnFilterSuppliersActionPerformed

