    private void btnConfirmAddSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmAddSupplierActionPerformed

        // Capture the supplier details
        String supplierCode = txtAddSupplierCode.getText().trim();
        String supplierName = txtAddSupplierName.getText().trim();
        String headquarters = txtAddHeadquarters.getText().trim();

        if(supplierCode.isEmpty() || supplierName.isEmpty() || headquarters.isEmpty()){
            JOptionPane.showMessageDialog(this,"Code, Name and Location cannot be blank","Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!supplierCode.matches("S\\d{3}")) {
            JOptionPane.showMessageDialog(this, "Supplier code must start with 'S' followed by 3 digits.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean codeExists = supplierList.stream().anyMatch(supplier -> supplier.getSupplierCode().equalsIgnoreCase(supplierCode));
        if(codeExists){
            JOptionPane.showMessageDialog(this, "Supplier code already exists.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean nameExists = supplierList.stream().anyMatch(supplier -> supplier.getSupplierName().equalsIgnoreCase(supplierName));
        if(nameExists){
            JOptionPane.showMessageDialog(this, "Supplier name already exists.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Get the selected item codes
        ArrayList<String> selectedItemCodes = new ArrayList<>(lstItems.getSelectedValuesList());  // Convert to ArrayList

        // Create the new supplier
        Supplier newSupplier = new Supplier(supplierCode, supplierName, headquarters, selectedItemCodes);
        
        supplierList.clear();
        supplierManager.read(supplierList, Supplier.class);

        // Add the supplier and associate the items
        boolean success = Supplier.addSupplier(supplierList, itemList, newSupplier);

        // Update the JTable to show the new supplier
        if (success) {
            // Add to the JTable
            loadSupplierTable();

            JOptionPane.showMessageDialog(this, "Supplier added successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Supplier with this code or name already exists.");
        }
    }//GEN-LAST:event_btnConfirmAddSupplierActionPerformed

