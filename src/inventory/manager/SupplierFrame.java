    private void loadSupplierTable() {
        supplierList.clear();
        // Load items from the file into the ArrayList
        supplierManager.read(supplierList, Supplier.class);

        // Get the table model
        DefaultTableModel model = (DefaultTableModel) tblSupplierTable.getModel();
        model.setRowCount(0);  // Clear existing rows
        
        for (Supplier supplier : supplierList) {
            ArrayList<String> suppliedItemCodes = supplier.getSuppliedItemCodes();
            String suppliedItemCodesStr = (suppliedItemCodes != null && !suppliedItemCodes.isEmpty()) ? String.join(";", suppliedItemCodes) : "NONE";
            
            model.addRow(new Object[]{
                supplier.getSupplierCode(),
                supplier.getSupplierName(),
                supplier.getHqLocation(),
                suppliedItemCodesStr,
            });
        }
        

        // Optionally, handle case when no items are found
        if (supplierList.isEmpty()) {
            lblNoMatchMsg.setVisible(true); // Show no items found message
        } else {
            lblNoMatchMsg.setVisible(false); // Hide the message
        }
    }

