    private void btnConfirmModifySupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmModifySupplierActionPerformed
        String supplierCode = (String) cbxSupplierCode.getSelectedItem();
        String supplierName = txtSupplierName.getText().trim();
        String headquarters = txtHeadquarters.getText().trim();
        String suppliedItemCodesStr = txtSuppliedItemCodes.getText().trim();
        
        supplierList.clear();
        supplierManager.read(supplierList, Supplier.class);
        itemList.clear();
        itemManager.read(itemList, Item.class);
        // Validation checks
        if (supplierName.isEmpty() || headquarters.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Supplier Name and Headquarters cannot be blank.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean nameExists = supplierList.stream().anyMatch(supplier -> supplier.getSupplierName().equalsIgnoreCase(supplierName) &&!supplier.getSupplierCode().equals(supplierCode));
        if(nameExists){
            JOptionPane.showMessageDialog(this, "Supplier name already exists.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
       
        ArrayList<String> itemCodes = new ArrayList<>(Arrays.asList(suppliedItemCodesStr.split(";")));

        boolean success = Supplier.modifySupplier(supplierList, itemList, supplierCode,supplierName,headquarters,itemCodes);
        
        if (success) {
             loadSupplierTable();
             JOptionPane.showMessageDialog(this, "Supplier modified successfully!");
         } else {
             JOptionPane.showMessageDialog(this, "One or more item codes are invalid.");
        }
    }//GEN-LAST:event_btnConfirmModifySupplierActionPerformed

