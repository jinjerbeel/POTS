    private void btnFilterItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterItemsActionPerformed

        String itemCodeInput = txtItemCode.getText();
        String itemNameInput = (String) cbxItemName.getSelectedItem();
        String categoryInput = (String) cbxCategory.getSelectedItem();
        String comparisonOperatorInput = (String) cbxComparisonOperator.getSelectedItem();
        String quantityValueInput = txtQuantityValue.getText();

        boolean viewAllSellingPrice = chkViewAllSellingPrice.isSelected();
        String minSellingPriceInput = txtMinSellingPrice.getText();
        String maxSellingrPiceInput = txtMaxSellingPrice.getText();

        String supplierIDInput = txtSupplierID.getText();

        List<String> matchingRows = Item.filterItemsTable(
            itemCodeInput,
            itemNameInput,
            categoryInput,
            comparisonOperatorInput,
            quantityValueInput,
            viewAllSellingPrice,
            minSellingPriceInput,
            maxSellingrPiceInput,
            supplierIDInput
        );

        DefaultTableModel tableModel = (DefaultTableModel) tblItemsTable.getModel();

        tableModel.setRowCount(0);

        if (!matchingRows.isEmpty()) {
            for(String row : matchingRows) {
                String[] parts = row.split(",");
                String itemCode = parts[0].trim();
                String itemName = parts[1].trim();
                String category = parts[2].trim();
                String quantity = parts[3].trim();
                String sellingPrice = parts[4].trim(); 
                String supplierCodes = String.join(";", Arrays.asList(parts).subList(5, parts.length));
                
                tableModel.addRow(new Object[]{itemCode,itemName,category, quantity,sellingPrice,supplierCodes});
                
            }
            lblNoMatchMsg.setVisible(false);
        } else {
            lblNoMatchMsg.setVisible(true);
            lblNoMatchMsg.setText("No Matching Items");
        }

    }//GEN-LAST:event_btnFilterItemsActionPerformed

