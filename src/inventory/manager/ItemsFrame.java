    private void btnConfirmModifyItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmModifyItemsActionPerformed
        String itemCode = (String) cbxItemCode.getSelectedItem();
        String itemName = txtModifyItemName.getText().trim();
        String category = txtModifyCategory.getText().trim();
        String quantityStr = txtModifyQuantity.getText().trim();
        String sellingPriceStr = txtModifySellingPrice.getText().trim();
        String supplierCodes = txtModifySupplier.getText().trim();
        
        supplierList.clear();
        supplierManager.read(supplierList, Supplier.class);
        itemList.clear();
        itemManager.read(itemList, Item.class);
        // Validation checks
        if(itemCode.equals("NONE")){
            JOptionPane.showMessageDialog(this, "Select an item to modify.");
            return;     
        }
        if(itemName.isEmpty() || category.isEmpty()){
            JOptionPane.showMessageDialog(this,"Name and Category cannot be blank.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean nameExists = itemList.stream().anyMatch(item -> item.getItemName().equalsIgnoreCase(itemName)&&!item.getItemCode().equals(itemCode));
        if(nameExists){
            JOptionPane.showMessageDialog(this, "Item name already exists.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int quantity;
        double sellingPrice;
        
        try{
            quantity = Integer.parseInt(quantityStr);
            if (quantity < 0) { // Check for negative quantity
                JOptionPane.showMessageDialog(this, "Quantity cannot be negative.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Quantity must be an integer","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            sellingPrice = Double.parseDouble(sellingPriceStr);
            if (sellingPrice < 0) { // Check for negative cost or selling price
                JOptionPane.showMessageDialog(this, "Cost and Selling Price cannot be negative.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Cost and Selling Price must be valid numbers.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
       
        ArrayList<String> suppliers = new ArrayList<>(Arrays.asList(supplierCodes.split(";")));

        boolean success = Item.modifyItem(itemList, supplierList, itemCode,itemName,category,quantity,sellingPrice,suppliers);
        
        if (success) {
            loadItemsTable();
            JOptionPane.showMessageDialog(this, "Item modified successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "One or more supplier codes are invalid.","Input Error",JOptionPane.ERROR_MESSAGE);
        }
                   
    }//GEN-LAST:event_btnConfirmModifyItemsActionPerformed

