    private void btnConfirmAddItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmAddItemsActionPerformed

        // Capture the supplier details
        String itemCode = txtAddItemCode.getText().trim();
        String itemName = txtAddItemName.getText().trim();
        String category = txtAddCategory.getText().trim();
        String quantityStr = txtAddQuantity.getText().trim();
        String sellingPriceStr = txtAddSellingPrice.getText().trim();

        if(itemCode.isEmpty() || itemName.isEmpty() || category.isEmpty()){
            JOptionPane.showMessageDialog(this,"Code, Name and Category cannot be blank.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!itemCode.matches("I\\d{3}")) {
            JOptionPane.showMessageDialog(this, "Item code must start with 'I' followed by 3 digits.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean codeExists = itemList.stream().anyMatch(item -> item.getItemCode().equalsIgnoreCase(itemCode));
        if(codeExists){
            JOptionPane.showMessageDialog(this, "Item code already exists.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean nameExists = itemList.stream().anyMatch(item -> item.getItemName().equalsIgnoreCase(itemName));
        if(nameExists){
            JOptionPane.showMessageDialog(this, "Item name already exists.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int quantity;
        double sellingPrice;
        try{
            quantity = Integer.parseInt(quantityStr);
            if (quantity < 0) { // Check for negative quantity
                JOptionPane.showMessageDialog(this, "Quantity cannot be negative.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Quantity must be an integer","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            sellingPrice = Double.parseDouble(sellingPriceStr);
            if (sellingPrice < 0) { // Check for negative cost or selling price
                JOptionPane.showMessageDialog(this, "Selling Price cannot be negative.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Selling Price must be valid numbers.","Input Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Get the selected item codes
        ArrayList<String> selectedSupplierCodes = new ArrayList<>(lstSuppliers.getSelectedValuesList());  // Convert to ArrayList

        // Create the new supplier
        Item newItem = new Item(itemCode, itemName,category, quantity,sellingPrice,selectedSupplierCodes);

        itemList.clear();
        itemManager.read(itemList, Item.class);
        // Add the supplier and associate the items
        boolean success = Item.addItem(itemList,supplierList,  newItem);

        // Update the JTable to show the new supplier
        if (success) {
            // Add to the JTable
            loadItemsTable();
            JOptionPane.showMessageDialog(this, "Item added successfully!");
        }
    }//GEN-LAST:event_btnConfirmAddItemsActionPerformed

