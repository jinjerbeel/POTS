    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        String enteredText = jTextField1.getText().trim(); // Get and trim text from the text field

        if (enteredText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Supplier ID to search.");
            return;
        }

        try {
            DefaultTableModel model = (DefaultTableModel) jTable5.getModel();
            model.setRowCount(0); // Clear existing rows in the table

            boolean found = false; // Initialize a flag to track matches

            // Read Suppliers.txt and store supplier info in a HashMap
            HashMap<String, String> supplierMap = new HashMap<>();
            try (BufferedReader brSuppliers = new BufferedReader(new FileReader("Suppliers.txt"))) {
                String supplierLine;

                while ((supplierLine = brSuppliers.readLine()) != null) {
                    String[] fields = supplierLine.split(",");
                    if (fields.length >= 4) {  // Validate the fields
                        String supplierID = fields[0].trim(); // Trim any whitespace
                        String companyName = fields[1].trim();
                        String location = fields[2].trim();
                        String items = fields[3].trim();

                        supplierMap.put(supplierID, companyName + "," + location + "," + items);
                    }
                }
            }

            // Read PaymentHistory.txt and match records with suppliers
            try (BufferedReader brPaymentHistory = new BufferedReader(new FileReader("PaymentHistory.txt"))) {
                String paymentLine;

                while ((paymentLine = brPaymentHistory.readLine()) != null) {
                    String[] fields = paymentLine.split(",");
                    if (fields.length >= 12) {  // Validate the fields
                        String Po_number = fields[0].trim();
                        String Requisition_ID = fields[1].trim();
                        String Date = fields[2].trim();
                        String Supplier_ID = fields[3].trim();
                        String SupplierName = fields[4].trim();
                        String Item_Code = fields[5].trim();
                        String Item_Name = fields[6].trim();
                        String Quantity = fields[7].trim();
                        String Unit_Price = fields[8].trim();
                        String Total_Price = fields[9].trim();
                        String Approved_By = fields[10].trim();
                        String Status = fields[11].trim();

                        // Retrieve supplier details from the map
                        String supplierInfo = supplierMap.get(Supplier_ID);
                        if (supplierInfo != null) {
                            String[] supplierDetails = supplierInfo.split(",");
                            if (supplierDetails.length >= 3) {
                                String companyName = supplierDetails[0];
                                String location = supplierDetails[1];
                                String items = supplierDetails[2];

                                // Match the entered text with Supplier_ID
                                if (Supplier_ID.equalsIgnoreCase(enteredText)) {
                                    found = true; // Match found
                                    model.addRow(new Object[]{
                                        Supplier_ID, companyName, Po_number, Requisition_ID,
                                        Date, Item_Code, Item_Name, Quantity, location, Status
                                    });
                                }
                            }
                        }
                    }
                }
            }

            // Notify user if no matches were found
            if (!found) {
                JOptionPane.showMessageDialog(this, "No matching Supplier ID found.");
            }

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Unexpected error: " + e.getMessage());
        }


    }//GEN-LAST:event_jButton12ActionPerformed

