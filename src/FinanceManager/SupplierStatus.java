    public void refresh_data() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable5.getModel();
            model.setRowCount(0);// Clear existing rows in the table

            // Read Suppliers.txt and store the supplier info in a map
            HashMap<String, String> supplierMap = new HashMap<>();
            FileReader frSuppliers = new FileReader("Suppliers.txt");
            BufferedReader brSuppliers = new BufferedReader(frSuppliers);
            String supplierLine;

            while ((supplierLine = brSuppliers.readLine()) != null) {
                String[] fields = supplierLine.split(",");
                if (fields.length >= 4) {  // Check if the line has enough fields
                    String supplierID = fields[0];
                    String companyName = fields[1];
                    String location = fields[2];
                    String items = fields[3];

                    // Store the supplier information in the map
                    supplierMap.put(" " + supplierID, companyName + "," + location + "," + items);
                }
            }

            brSuppliers.close();
            frSuppliers.close();

            // Now read PaymentHistory.txt and match with suppliers
            FileReader frPaymentHistory = new FileReader("PaymentHistory.txt");
            BufferedReader brPaymentHistory = new BufferedReader(frPaymentHistory);
            String PaymentLine;

            while ((PaymentLine = brPaymentHistory.readLine()) != null) {
                String[] fields = PaymentLine.split(",");
                if (fields.length >= 11) {  // Check if the line has enough fields
                    String Po_number = fields[0];
                    String Requisition_ID = fields[1];
                    String Date = fields[2];
                    String Supplier_ID = fields[3];
                    String SupplierName = fields[4];
                    String Item_Code = fields[5];
                    String Item_Name = fields[6];
                    String Quantity = fields[7];
                    String Unit_Price = fields[8];
                    String Total_Price = fields[9];
                    String Approved_By = fields[10];
                    String Status = fields[11];

                    // Get supplier information from the map
                    String supplierInfo = supplierMap.get(Supplier_ID);
                    if (supplierInfo != null) {
                        String[] supplierDetails = supplierInfo.split(",");
                        if (supplierDetails.length >= 3) {  // Ensure supplier info is complete
                            String companyName = supplierDetails[0];
                            String location = supplierDetails[1];
                            String items = supplierDetails[2];

                            // Add a new row with combined information to the table
                            model.addRow(new Object[]{Supplier_ID, companyName, Po_number, Requisition_ID, Date, Item_Code, Item_Name, Quantity, location, Status});
                        }
                    }
                }
            }

            brPaymentHistory.close();
            frPaymentHistory.close();

        } catch (IOException e) {
            // Show an error message if there is an issue with file reading
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

