    public void refresh_data() {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
            model.setRowCount(0);

            FileReader fr = new FileReader(Config.getInventoryPath());
            BufferedReader br = new BufferedReader(fr);
            String read;

            StockClass stock = new StockClass();

            while ((read = br.readLine()) != null) {
                // Populate the StockClass object
                stock.StockClassEntry(
                        read.split(",")[0], 
                        read.split(",")[1], 
                        read.split(",")[2], 
                        read.split(",")[3], 
                        read.split(",")[4], 
                        read.split(",")[5] 
                );

                // Add the data to the table model
                model.addRow(new Object[]{
                    stock.getItemID(),
                    stock.getItemName(),
                    stock.getCategory(),
                    stock.getQuantity(),
                    stock.getPrice(),
                    stock.getSupplierID()
                });
            }

            br.close();
            fr.close();

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

